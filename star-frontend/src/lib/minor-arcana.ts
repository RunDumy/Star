// Helper functions
export function getMinorArcanaCard(name: string): MinorArcanaCard | undefined {
  return MINOR_ARCANA_CARDS.find(card => card.name === name);
}

export function getCardsBySuit(suit: 'cups' | 'pentacles' | 'swords' | 'wands'): MinorArcanaCard[] {
  return MINOR_ARCANA_CARDS.filter(card => card.suit === suit);
}

export function getCardsByElement(element: string): MinorArcanaCard[] {
  return MINOR_ARCANA_CARDS.filter(card => card.element === element);
}

export function getAllMinorArcanaNames(): string[] {
  return MINOR_ARCANA_CARDS.map(card => card.name);
}
